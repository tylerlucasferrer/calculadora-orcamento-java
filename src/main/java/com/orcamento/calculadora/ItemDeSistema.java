package com.orcamento.calculadora;

/**
 *
 * @author r.prado.dos.santos
 */
public class ItemDeSistema {
    private String tipo;
    private int dificuldade;
    private int tempoEmpregado;

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the dificuldade
     */
    public int getDificuldade() {
        return dificuldade;
    }

    /**
     * @param dificuldade the dificuldade to set
     */
    public void setDificuldade(int dificuldade) {
        this.dificuldade = dificuldade;
    }

    /**
     * @return the tempoEmpregado
     */
    public int getTempoEmpregado() {
        return tempoEmpregado;
    }

    /**
     * @param tempoEmpregado the tempoEmpregado to set
     */
    public void setTempoEmpregado(int tempoEmpregado) {
        this.tempoEmpregado = tempoEmpregado;
    }
    
    
}
